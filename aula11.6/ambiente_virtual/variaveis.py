# variaveis sao usadas para salvar algo na memoria do computador
# o sinal de = é utilizado para a atribuiçao de valores
var01 = "alguma coisa"
print(var01)

var01 = "outra coisa"
print(var01)

var01 = 10
print(var01)

var02 = "a" + "b" + "c"
print(var02)

var02 = "a" * 10
print(var02)

var02 = 10 * "paulo "
print(var02)
